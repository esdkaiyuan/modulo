/**
 * Export a small preview image (for sharing / thumbnails).
 */
export function exportPreviewImage(pattern: PatternResult): void {
  const canvas = renderPreview(pattern, 600);
  canvas.toBlob((blob) => {
    if (!blob) return;
    downloadBlob(blob, `bead_preview_${pattern.width}x${pattern.height}.png`);
  }, 'image/png');
}

