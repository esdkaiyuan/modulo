/**
 * Export the materials list as CSV.
 */
export function exportCsv(pattern: PatternResult, title?: string): void {
  const brand = getBrand(pattern.brandId);
  const rows = [
    ['Symbol', 'Code', 'Color Name', 'Hex', 'Count', 'Percentage', 'Brand'].join(','),
    ...pattern.materials.map((m) =>
      [
        m.symbol,
        m.bead.code,
        `"${m.bead.name}"`,
        m.bead.hex,
        m.count,
        m.percentage.toFixed(1) + '%',
        brand.name
      ].join(',')
    )
  ];
  rows.push('');
  rows.push(`"Total",,,,${pattern.totalBeads},,"${pattern.width}×${pattern.height}"`);

  const content = rows.join('\n');
  const blob = new Blob(['﻿' + content], { type: 'text/csv;charset=utf-8' });
  downloadBlob(blob, sanitizeFilename(title || `bead_materials_${pattern.width}x${pattern.height}`) + '.csv');
}

/**
 * Export the full pattern data as JSON.
 */
export function exportJson(pattern: PatternResult, title?: string): void {
  const data = {
    title: title || '',
    width: pattern.width,
    height: pattern.height,
    brand: pattern.brandId,
    boardSize: pattern.boardSize,
    totalBeads: pattern.totalBeads,
    materials: pattern.materials.map((m) => ({
      symbol: m.symbol,
      code: m.bead.code,
      name: m.bead.name,
      hex: m.bead.hex,
      count: m.count,
      percentage: +m.percentage.toFixed(1)
    })),
    grid: pattern.cells.map((row) =>
      row.map((cell) => cell?.bead?.code ?? null)
    )
  };

  const content = JSON.stringify(data, null, 2);
  const blob = new Blob([content], { type: 'application/json;charset=utf-8' });
  downloadBlob(blob, sanitizeFilename(title || `bead_pattern_${pattern.width}x${pattern.height}`) + '.json');
}

// ── Helpers ──

function downloadBlob(blob: Blob, filename: string) {
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  a.click();
  URL.revokeObjectURL(url);
}

function sanitizeFilename(name: string): string {
  return name.replace(/[^a-zA-Z0-9_\-一-鿿]/g, '_').replace(/_+/g, '_');
}
