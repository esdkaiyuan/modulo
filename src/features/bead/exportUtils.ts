import type { PatternResult } from './types';
import { renderPattern, renderPreview } from './patternRenderer';
import { getBrand } from './paletteData';

/**
 * Export the pattern as a PNG image.
 */
export function exportPng(pattern: PatternResult, title?: string): void {
  const canvas = renderPattern(pattern, {
    cellSize: 24,
    showGrid: true,
    showCoordinates: true,
    showBoardLines: true,
    showLegend: true,
    title: title || `Bead Pattern — ${pattern.width}×${pattern.height}`
  });

  canvas.toBlob((blob) => {
    if (!blob) return;
    downloadBlob(blob, sanitizeFilename(title || `bead_pattern_${pattern.width}x${pattern.height}`) + '.png');
  }, 'image/png');
}

